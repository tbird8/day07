char *ft_strdup(char *src)
{
	int s;
     s = 0;
	int i;
     i = 0;

   char *dest;

	while (src[s] != '\0')
		s++;
	dest = (char *)malloc(sizeof(*dest) * (s + 1));
	while (i < s)
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}

